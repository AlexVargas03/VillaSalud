    private void listado() {
        //Limpiar la tabla
        modeloDist.setRowCount(0);
        if (daoDistribuidor.selectTodo() == null) {
            return;
        }
        for (Distribuidor dist : daoDistribuidor.selectTodo()) {
            Object[] fila = new Object[2];
            fila[0] = dist.getId();
            fila[1] = dist.getNombre();
            modeloDist.addRow(fila);
        }
    }

    private void habilitar(boolean b) {
        txt_dist_nombre.setEnabled(b);
        btn_dist_confirmar.setEnabled(b);
        btn_dist_cancelar.setEnabled(b);

        btn_dist_agregar.setEnabled(!b);
        btn_dist_editar.setEnabled(!b);
        btn_dist_eliminar.setEnabled(!b);
    }

    private void limpiar() {
        txt_dist_nombre.setText(null);
        opcion = "";
    }
