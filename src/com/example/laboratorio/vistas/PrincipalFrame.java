    private void btn_dist_confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dist_confirmarActionPerformed
        String nombre = txt_dist_nombre.getText();
        distribuidor.setNombre(nombre);

        switch (opcion) {
            case "AGREGAR" -> {
                try {
                    daoDistribuidor.insertar(distribuidor);
                    listado();
                    limpiar();
                    habilitar(false);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, daoDistribuidor.getMensaje());
                }
            }
            case "EDITAR" -> {
                try {
                    daoDistribuidor.actualizar(distribuidor);
                    listado();
                    limpiar();
                    habilitar(false);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, daoDistribuidor.getMensaje());
                }
            }
        }
    }//GEN-LAST:event_btn_dist_confirmarActionPerformed

    private void btn_dist_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dist_editarActionPerformed
        if (tablaDistribuidor.getSelectedRow() != -1) {
            opcion = "EDITAR";
            habilitar(true);
            distribuidor.setId(Integer.valueOf(
                    tablaDistribuidor.getValueAt(
                            tablaDistribuidor.getSelectedRow(), 0)
                            .toString())
            );
            txt_dist_nombre.setText(
                    tablaDistribuidor.getValueAt(
                            tablaDistribuidor.getSelectedRow(), 1)
                            .toString()
            );
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Seleccione una fila");

        }


    }//GEN-LAST:event_btn_dist_editarActionPerformed

    private void btn_dist_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dist_eliminarActionPerformed
        if (tablaDistribuidor.getSelectedRow() != -1) {
            try {
                daoDistribuidor.eliminar(
                Integer.valueOf(
                    tablaDistribuidor.getValueAt(
                            tablaDistribuidor.getSelectedRow(), 0)
                            .toString())
                ); 
                limpiar();
                listado();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, daoDistribuidor.getMensaje());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Seleccione una fila");
        }
    }//GEN-LAST:event_btn_dist_eliminarActionPerformed

